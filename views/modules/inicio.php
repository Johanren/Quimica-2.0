                <p>Un oxoácido es un ácido que contiene oxígeno. Específicamente, es un compuesto que contiene hidrógeno, oxígeno y al menos otro elemento, con al menos un átomo de hidrógeno unido al oxígeno que puede disociarse para producir el catión H+ y el anión del ácido.1​ Esto quiere decir que son compuestos ternarios (formados por tres tipos de átomos), y que siempre mantienen el mismo orden (Hidrógeno, 1 no metal y oxígeno). También podemos decir que se forman por reacción de un óxido no metálico y agua.

                  Su fórmula responde al patrón HaAbOc, donde A es un no metal o metal de transición.

                  Ejemplos de oxácidos:

                  Ácido sulfúrico (H2SO4). Formado por la combinación de una molécula de H2O con una molécula de óxido de azufre SO3:
                SO3 + H2O → H2SO4</p>
