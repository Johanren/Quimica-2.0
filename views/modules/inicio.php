                  Los hidróxidos se clasifican en: básicos, anfóteros y ácidos.1​ Por ejemplo, el Zn(OH)2 es un hidróxido anfótero ya que:

                  con ácidos: Zn(OH)2 + 2H+ → Zn+2 + 2H2O
                con bases: Zn(OH)2 + 2OH− → [Zn(OH)4]−2</p>
